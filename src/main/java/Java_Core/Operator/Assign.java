package Java_Core.Operator;

public class Assign {
    public static void main(String[] args) {
        //+=,-=,*=
        int beta=10;
        int alpha=20;
        int cosmo=0;
        beta+=50;
        System.out.println("Beta value is :"+beta);
        cosmo+=100;
        System.out.println("Cosmo value :"+cosmo);
        alpha-=30;
        System.out.println("Alpha value "+alpha);
        beta*=10;
        System.out.println("Beta value:"+beta);

    }
}
