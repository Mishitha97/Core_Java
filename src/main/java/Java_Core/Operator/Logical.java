package Java_Core.Operator;

public class Logical {
    public static void main(String[] args) {
        int alpha=20;
        int beta=40;
        int cosmo=10;
        System.out.println(alpha>beta && beta<cosmo);
        System.out.println(cosmo>alpha||beta<alpha);
        System.out.println(alpha!=beta);

    }
}
